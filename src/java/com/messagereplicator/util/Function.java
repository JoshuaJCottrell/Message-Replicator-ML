/**
 * 
 * @author user Joshua Cottrell
 * 
 * A class that allows the passing of maths functions into arguments
 *
 */
public abstract class Function {

	/**
	 * Performs a mathematical function on x
	 * 
	 * @param x
	 * @return f(x)
	 */
	public abstract float perform(float x);
	
}
